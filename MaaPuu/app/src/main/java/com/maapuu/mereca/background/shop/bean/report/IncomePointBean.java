public class IncomePointBean {
    private String catalog_id;
    private String catalog_name;
    private String num;
    private String amount;
    private String color;
    private String num_percent;
    private String amount_percent;

    public String getCatalog_id() {
        return catalog_id;
    }

    public void setCatalog_id(String catalog_id) {
        this.catalog_id = catalog_id;
    }

    public String getCatalog_name() {
        return catalog_name;
    }

    public void setCatalog_name(String catalog_name) {
        this.catalog_name = catalog_name;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getNum_percent() {
        return num_percent;
    }

    public void setNum_percent(String num_percent) {
        this.num_percent = num_percent;
    }

    public String getAmount_percent() {
        return amount_percent;
    }

    public void setAmount_percent(String amount_percent) {
        this.amount_percent = amount_percent;
    }
}
